let randomSpecies = function() { return faker.random.arrayElement(exoticSpecies); };
let randomSex = function() { return faker.random.arrayElement(['M','F','U']); };
let randomDate = function() {let date = faker.date.past(); return `${date.getFullYear()}-${date.getMonth()+1}-${date.getDate()}`};

// name, species, sex, neutered, birthdate
let fakeExotic = [faker.name.firstName, randomSpecies, randomSex, faker.random.boolean, randomDate];

let exoticsArray = [];
for(let exotic = 0; exotic < 1000; exotic++)
{
  let fakedExotic = fakeExotic.map(f => f.call());
  fakedExotic = {
    name: fakedExotic[0], species: fakedExotic[1].slice(0,64), sex: fakedExotic[2], 
    neutered: fakedExotic[3] ? 1 : 0, birthdate: fakedExotic[4]
  };
  exoticsArray.push(fakedExotic);
}

let csvExotics = json2csv(exoticsArray,['name','species','sex','neutered','birthdate']);
